# AND
print( True and True)
print( True and False)
print( False & True)
print( False & False)
print("-----------------")

# OR
print( True or True)
print( True or False)
print( False | True)
print( False | False)
print("-----------------")

# NOT
print( not True)
print( not False)
print( False & (not True))
print( False | (not False))
print("-----------------")

# XOR
print( True ^ True)
print( True ^ False)
print( False ^ True)
print( False ^ False)
print("-----------------")
